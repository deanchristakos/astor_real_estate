from marshmallow import Schema, fields

class UnitSchema(Schema):
    id = fields.Str()
    gr_sqft = fields.Int()
    aptno = fields.Str()

class CondoUnitSchema(UnitSchema):
    bbl = fields.Str()
    cur_fv_l = fields.Int()
    cur_fv_t = fields.Int()
    new_fv_l = fields.Int()
    new_fv_t = fields.Int()
    curavl = fields.Int()
    curavt = fields.Int()
    curexl = fields.Int()
    curext = fields.Int()
    curavl_a = fields.Int()
    curavt_a = fields.Int()
    curexl_a = fields.Int()
    curext_a = fields.Int()
    tn_avt = fields.Int()
    tn_avl = fields.Int()
    tn_ext = fields.Int()
    tn_avl_a = fields.Int()
    tn_avt_a = fields.Int()
    tn_exl_a = fields.Int()
    tn_ext_a = fields.Int()
    fn_avl = fields.Int()
    fn_avt = fields.Int()
    fn_exl = fields.Int()
    fn_avl_a = fields.Int()
    fn_avt_a = fields.Int()
    fn_exl_a = fields.Int()
    fn_ext_a = fields.Int()
    property_tax = fields.Float()
    sales = fields.List(fields.Nested('PropertySaleSchema'))

class PropertySaleSchema(Schema):
    price = fields.Float()
    date = fields.Date()
    seller = fields.Str()
    buyer = fields.Str()

class BuildingSchema(Schema):
    bbl = fields.Str()
    address = fields.Str()
    lotarea = fields.Int()
    bldgarea = fields.Int()
    comarea = fields.Int()
    resarea = fields.Int()
    officearea = fields.Int()
    retailarea = fields.Int()
    garagearea = fields.Int()
    strgearea = fields.Int()
    factryarea = fields.Int()
    otherarea = fields.Int()
    gr_sqft = fields.Int()
    property_tax = fields.Float()
    numfloors = fields.Int()
    unitsres = fields.Int()
    unitstotal = fields.Int()
    yearbuilt = fields.Int()
    yearalter1 = fields.Int()
    yearalter2 = fields.Int()
    xcoord = fields.Int()
    ycoord = fields.Int()
    nearby_buildings = fields.List(fields.Nested('BuildingSchema'))
    sales = fields.List(fields.Nested('PropertySaleSchema'))
    units = fields.List(fields.Nested('UnitSchema'))

class CondoBuildingSchema(BuildingSchema):
    units = fields.List(fields.Nested('CondoUnitSchema'))
